QString getWorldName(int world_id, QString lang) {
    if (lang == "de_DE")
    {
        if (GW2_WORLD_NAMES_DE.isEmpty())
            GW2_WORLD_NAMES_DE = _Init_GW2_WORLD_NAMES_DE();

        return GW2_WORLD_NAMES_DE.value(world_id, "Unbekannt");
    }
    else if (lang == "fr_FR")
    {
        if (GW2_WORLD_NAMES_FR.isEmpty())
            GW2_WORLD_NAMES_FR = _Init_GW2_WORLD_NAMES_FR();

        return GW2_WORLD_NAMES_FR.value(world_id, "Unknown");
    }
    else if (lang == "es_ES")
    {
        if (GW2_WORLD_NAMES_ES.isEmpty())
            GW2_WORLD_NAMES_ES = _Init_GW2_WORLD_NAMES_ES();

        return GW2_WORLD_NAMES_ES.value(world_id, "Unknown");
    }
    else
    {
        if (GW2_WORLD_NAMES_EN.isEmpty())
            GW2_WORLD_NAMES_EN = _Init_GW2_WORLD_NAMES_EN();

        return GW2_WORLD_NAMES_EN.value(world_id, "Unknown");
    }
}

